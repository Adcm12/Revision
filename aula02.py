nome = "Adrian"
sobrenome = "Castillo"
idade = 19

#Concatencao
print('Nome', nome, "Sobrenome ", sobrenome, "Idade ", idade )

#interpolacao
print(f'Nome {nome} Sobrenome {sobrenome} Idade {idade}')

#mascara de subtituicao
print('Nome {} Sobrenome {} Idade {}'.format(nome, sobrenome, idade))

